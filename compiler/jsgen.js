import { rootNode, childOfType, walk, stringLiteralValue } from './tree.js';
import { analyzeHostRequirements } from './host_analysis.js';

export function jsgen(treeOrNode, binary, { mode = 'program', host = null } = {}) {
    const root = rootNode(treeOrNode);
    const activeHost = host ?? analyzeHostRequirements(root);
    const { strings, exportNames } = analyze(root, mode);
    const moduleImports = groupImportsByModule(activeHost);
    const needsImports = moduleImports.length > 0;
    const needsNodeModuleResolution = moduleImports.some(group => group.autoResolve);
    const needsImportExceptionWrappers = activeHost.importFns.some(item => item.exceptionFallback !== null);
    const lines = [
        '// Generated by utu compiler — do not edit',
        '',
        `const __b64 = "${toBase64(binary)}";`,
        '',
        'const __decodeBase64 = s => {',
        '  const bin = atob(s), out = new Uint8Array(bin.length);',
        '  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);',
        '  return out;',
        '};',
    ];

    if (strings.length) lines.push('', `const __strings = [${strings.map(value => JSON.stringify(value)).join(', ')}];`);
    lines.push('', 'const __jsStringBuiltins = {');
    lines.push('  length: value => value.length,');
    lines.push('  charCodeAt: (value, index) => value.charCodeAt(index),');
    lines.push('  concat: (left, right) => `${left}${right}`,');
    lines.push('  substring: (value, start, end) => value.substring(start, end),');
    lines.push('  equals: (left, right) => left === right ? 1 : 0,');
    lines.push('  fromCharCode: value => String.fromCharCode(value),');
    lines.push('};');

    if (needsImports) {
        lines.push('', 'const __requireHostBinding = (moduleObject, moduleName, importName) => {');
        lines.push('  if (!moduleObject || (typeof moduleObject !== "object" && typeof moduleObject !== "function") || !(importName in moduleObject)) {');
        lines.push('    throw new Error(`Missing host import "${importName}" from "${moduleName}"`);');
        lines.push('  }');
        lines.push('  return moduleObject[importName];');
        lines.push('};');
    }

    if (needsNodeModuleResolution) {
        lines.push('', 'const __importHostModule = async specifier => {');
        lines.push('  try {');
        lines.push('    return await import(specifier);');
        lines.push('  } catch (error) {');
        lines.push('    throw new Error(`Failed to auto-resolve host module "${specifier}": ${error instanceof Error ? error.message : String(error)}`);');
        lines.push('  }');
        lines.push('};');
    }

    if (needsImportExceptionWrappers) {
        lines.push('', 'const __wrapImportExceptionAsNull = (fn, fallback) => {');
        lines.push('  if (typeof fn !== "function") return fn;');
        lines.push('  return (...args) => {');
        lines.push('    try {');
        lines.push('      return fn(...args);');
        lines.push('    } catch (_error) {');
        lines.push('      return Array.isArray(fallback) ? fallback.slice() : fallback;');
        lines.push('    }');
        lines.push('  };');
        lines.push('};');
    }

    if (needsImports) lines.push('', '/** @param {Record<string, any>} imports */');

    lines.push('', `export async function instantiate(${needsImports ? 'imports = {}' : ''}) {`);
    if (needsImports) {
        for (const group of moduleImports) {
            if (!group.autoResolve) continue;
            lines.push(`  const ${group.ref} = imports[${JSON.stringify(group.module)}] ?? await __importHostModule(${JSON.stringify(group.module)});`);
        }
    }
    lines.push('  const module = await WebAssembly.compile(__decodeBase64(__b64));');
    lines.push('  const instance = await WebAssembly.instantiate(module, {');
    if (strings.length) lines.push('    "__strings": Object.fromEntries(__strings.map((s, i) => [String(i), s])),');
    lines.push('    "wasm:js-string": __jsStringBuiltins,');
    for (const group of moduleImports) {
        const moduleRef = group.autoResolve ? group.ref : `imports[${JSON.stringify(group.module)}]`;
        lines.push(`    ${JSON.stringify(group.module)}: {`);
        for (const entry of group.entries) {
            const binding = `__requireHostBinding(${moduleRef}, ${JSON.stringify(group.module)}, ${JSON.stringify(entry.name)})`;
            const value = entry.kind === 'function' && entry.exceptionFallback !== null
                ? `__wrapImportExceptionAsNull(${binding}, ${entry.exceptionFallback})`
                : binding;
            lines.push(`      ${JSON.stringify(entry.name)}: ${value},`);
        }
        lines.push('    },');
    }
    lines.push('  });', '  return instance.exports;', '}');
    if (exportNames.length) lines.push('', `// Exported functions: ${exportNames.join(', ')}`);

    return lines.join('\n');
}

function analyze(root, mode) {
    const strings = new Map(), exportNames = [];
    const fnBodies = [], globalBodies = [], testBodies = [], benchBodies = [];
    const addBody = (bucket, node) => node && bucket.push(node);

    for (const item of root.namedChildren) {
        switch (item.type) {
            case 'fn_decl':
                addBody(fnBodies, childOfType(item, 'block'));
                break;
            case 'export_decl': {
                const fn = childOfType(item, 'fn_decl');
                exportNames.push(childOfType(fn, 'identifier').text);
                addBody(fnBodies, childOfType(fn, 'block'));
                break;
            }
            case 'global_decl':
                addBody(globalBodies, item.namedChildren.at(-1));
                break;
            case 'test_decl':
                if (mode === 'test') addBody(testBodies, childOfType(item, 'block'));
                break;
            case 'bench_decl':
                if (mode === 'bench') {
                    const named = childOfType(item, 'setup_decl')?.namedChildren ?? [];
                    benchBodies.push(...named.slice(0, -1));
                    addBody(benchBodies, childOfType(named.at(-1), 'block'));
                }
                break;
        }
    }

    for (const node of [...fnBodies, ...globalBodies, ...testBodies, ...benchBodies]) walk(node, child => {
        const value = stringLiteralValue(child);
        if (value !== null && !strings.has(value)) strings.set(value, strings.size);
    });
    return { strings: [...strings.keys()], exportNames };
}

function groupImportsByModule({ importFns, importVals }) {
    const groups = new Map();
    const groupFor = (module) => {
        if (!groups.has(module)) groups.set(module, {
            module,
            entries: [],
            autoResolve: module.startsWith('node:'),
            ref: `__host_module_${groups.size}`,
        });
        return groups.get(module);
    };

    for (const entry of importFns) groupFor(entry.module).entries.push({ ...entry, kind: 'function' });
    for (const entry of importVals) groupFor(entry.module).entries.push({ ...entry, kind: 'value' });
    return [...groups.values()];
}

function toBase64(bytes) {
    let bin = '';
    for (const byte of bytes) bin += String.fromCharCode(byte);
    return btoa(bin);
}
