import { rootNode, childOfType, walk, stringLiteralValue } from './tree.js';
import { analyzeHostRequirements } from './host_analysis.js';

const JS_STRING_BUILTINS = [
    ['length', 'value => value.length'],
    ['charCodeAt', '(value, index) => value.charCodeAt(index)'],
    ['concat', '(left, right) => left + right'],
    ['substring', '(value, start, end) => value.substring(start, end)'],
    ['equals', '(left, right) => +(left === right)'],
    ['fromCharCode', 'value => String.fromCharCode(value)'],
];

export function jsgen(treeOrNode, binary, { mode = 'program', host = null, profile = null, shim = 'inline-wasm', moduleFormat = 'esm' } = {}) {
    if (shim !== 'inline-wasm' && shim !== 'external-wasm')
        throw new Error(`Unsupported shim "${shim}". Expected inline-wasm or external-wasm.`);
    if (moduleFormat !== 'esm')
        throw new Error(`Unsupported module format "${moduleFormat}". Expected esm.`);

    const root = rootNode(treeOrNode);
    const activeHost = host ?? analyzeHostRequirements(root);
    const { strings, exportNames } = analyze(root, mode);
    const moduleImports = groupImportsByModule(activeHost, profile);
    const needsImports = moduleImports.length > 0;
    const needsNodeImports = moduleImports.some(({ autoResolve }) => autoResolve);
    const needsExceptionWrappers = activeHost.importFns.some(({ exceptionFallback }) => exceptionFallback !== null);
    const lines = ['// Generated by utu compiler — do not edit'];

    if (shim === 'inline-wasm') lines.push(
        '',
        `const __b64 = "${toBase64(binary)}";`,
        'const __decodeBase64 = s => { const bin = atob(s), out = new Uint8Array(bin.length); for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i); return out; };',
        'const __wasmBytes = __decodeBase64(__b64);',
    );

    lines.push('', [
        'const __compileWasmModule = async wasmSource => {',
        '  if (wasmSource instanceof WebAssembly.Module) return wasmSource;',
        '  if (typeof Response !== "undefined" && wasmSource instanceof Response) {',
        '    if (!wasmSource.ok) throw new Error(`Failed to load wasm: ${wasmSource.status} ${wasmSource.statusText}`.trim());',
        '    return WebAssembly.compile(await wasmSource.arrayBuffer());',
        '  }',
        '  if (typeof wasmSource === "string" || (typeof URL !== "undefined" && wasmSource instanceof URL)) {',
        '    const wasmUrl = typeof URL !== "undefined" && wasmSource instanceof URL ? wasmSource : new URL(String(wasmSource), import.meta.url);',
        '    if (wasmUrl.protocol === "file:") {',
        '      try {',
        '        const [{ readFile }, { fileURLToPath }] = await Promise.all([import("node:fs/promises"), import("node:url")]);',
        '        return WebAssembly.compile(await readFile(fileURLToPath(wasmUrl)));',
        '      } catch (error) {',
        '        if (typeof fetch !== "function") throw new Error(`Failed to load file wasm: ${error instanceof Error ? error.message : String(error)}`);',
        '      }',
        '    }',
        '    if (typeof fetch !== "function") throw new Error("This generated shim needs fetch() or Node file access to load external wasm.");',
        '    const response = await fetch(wasmUrl);',
        '    if (!response.ok) throw new Error(`Failed to load wasm: ${response.status} ${response.statusText}`.trim());',
        '    return WebAssembly.compile(await response.arrayBuffer());',
        '  }',
        '  if (wasmSource instanceof ArrayBuffer || ArrayBuffer.isView(wasmSource)) return WebAssembly.compile(wasmSource);',
        '  throw new Error("Unsupported wasm source passed to instantiate().");',
        '};',
    ].join('\n'));

    if (shim === 'external-wasm') lines.push('', [
        'const __defaultWasmSource = () => {',
        '  if (import.meta.url.startsWith("blob:") || import.meta.url.startsWith("data:")) {',
        '    throw new Error("External wasm shims loaded from blob/data URLs require an explicit wasmSource argument.");',
        '  }',
        '  return new URL(import.meta.url.replace(/\\.(?:[cm]?js|mjs)$/u, ".wasm"));',
        '};',
    ].join('\n'));

    if (strings.length) lines.push('', `const __strings = [${strings.map(value => JSON.stringify(value)).join(', ')}];`);
    lines.push('', `const __jsStringBuiltins = {\n${JS_STRING_BUILTINS.map(([name, value]) => `  ${name}: ${value},`).join('\n')}\n};`);

    if (needsImports) lines.push('', [
        'const __globalObject = typeof globalThis !== "undefined" ? globalThis : undefined;',
        'const __importPaths = (moduleName, importName) => {',
        '  const paths = [[importName]];',
        '  const parts = importName.split("_").filter(Boolean);',
        '  if (parts.length > 1) paths.push(parts);',
        '  if (moduleName === "es" && parts.length > 1) {',
        '    const [head, ...tail] = parts;',
        '    const headVariants = [head];',
        '    const capitalized = head ? head[0].toUpperCase() + head.slice(1) : head;',
        '    if (capitalized && !headVariants.includes(capitalized)) headVariants.push(capitalized);',
        '    const upper = head.toUpperCase();',
        '    if (upper && !headVariants.includes(upper)) headVariants.push(upper);',
        '    for (const variant of headVariants.slice(1)) paths.push([variant, ...tail]);',
        '  }',
        '  return paths;',
        '};',
        'const __resolveBindingPath = (root, path) => {',
        '  if (!root || (typeof root !== "object" && typeof root !== "function")) return null;',
        '  let owner = null, value = root;',
        '  for (const segment of path) {',
        '    if (!value || (typeof value !== "object" && typeof value !== "function") || !(segment in value)) return null;',
        '    owner = value;',
        '    value = value[segment];',
        '  }',
        '  return { owner, value };',
        '};',
        'const __resolveHostBinding = (moduleObject, moduleName, importName) => {',
        '  const sources = [];',
        '  if (moduleObject !== undefined) sources.push(moduleObject);',
        '  if (moduleName === "es" && __globalObject !== undefined) sources.push(__globalObject);',
        '  for (const source of sources) {',
        '    for (const path of __importPaths(moduleName, importName)) {',
        '      const resolved = __resolveBindingPath(source, path);',
        '      if (resolved) return resolved;',
        '    }',
        '  }',
        '  throw new Error(`Missing host import "${importName}" from "${moduleName}"`);',
        '};',
        'const __bindHostFunction = (moduleObject, moduleName, importName, arity) => {',
        '  const { owner, value } = __resolveHostBinding(moduleObject, moduleName, importName);',
        '  if (typeof value !== "function") throw new Error(`Expected host function "${importName}" from "${moduleName}"`);',
        '  if (arity == null) return (...args) => value.apply(owner, args);',
        '  switch (arity) {',
        '    case 0: return () => value.call(owner);',
        '    case 1: return a0 => value.call(owner, a0);',
        '    case 2: return (a0, a1) => value.call(owner, a0, a1);',
        '    case 3: return (a0, a1, a2) => value.call(owner, a0, a1, a2);',
        '    case 4: return (a0, a1, a2, a3) => value.call(owner, a0, a1, a2, a3);',
        '    default: return (...args) => value.apply(owner, args);',
        '  }',
        '};',
        'const __readHostValue = (moduleObject, moduleName, importName) => __resolveHostBinding(moduleObject, moduleName, importName).value;',
    ].join('\n'));

    if (needsNodeImports) lines.push('', [
        'const __importHostModule = async specifier => {',
        '  try { return await import(specifier); }',
        '  catch (error) { throw new Error(`Failed to auto-resolve host module "${specifier}": ${error instanceof Error ? error.message : String(error)}`); }',
        '};',
    ].join('\n'));

    if (needsExceptionWrappers) lines.push('', [
        'const __wrapImportExceptionAsNull = (fn, fallback) => {',
        '  if (typeof fn !== "function") return fn;',
        '  return (...args) => {',
        '    try { return fn(...args); }',
        '    catch { return Array.isArray(fallback) ? fallback.slice() : fallback; }',
        '  };',
        '};',
    ].join('\n'));

    lines.push('', `export async function instantiate(wasmSource = ${shim === 'inline-wasm' ? '__wasmBytes' : '__defaultWasmSource()'}) {`);
    for (const group of moduleImports)
        if (group.autoResolve)
            lines.push(`  const ${group.ref} = await __importHostModule(${JSON.stringify(group.module)});`);
    lines.push('  const instance = await WebAssembly.instantiate(await __compileWasmModule(wasmSource), {');
    if (strings.length) lines.push('    "__strings": Object.fromEntries(__strings.map((s, i) => [i, s])),');
    lines.push('    "wasm:js-string": __jsStringBuiltins,');
    for (const group of moduleImports) {
            const moduleRef = group.autoResolve ? group.ref : group.module === 'es' ? '__globalObject' : 'undefined';
        lines.push(`    ${JSON.stringify(group.module)}: {`);
        for (const entry of group.entries) {
            const binding = entry.kind === 'function'
                ? `__bindHostFunction(${moduleRef}, ${JSON.stringify(group.module)}, ${JSON.stringify(entry.name)}, ${entry.paramCount ?? 'undefined'})`
                : `__readHostValue(${moduleRef}, ${JSON.stringify(group.module)}, ${JSON.stringify(entry.name)})`;
            const value = entry.kind === 'function' && entry.exceptionFallback !== null
                ? `__wrapImportExceptionAsNull(${binding}, ${entry.exceptionFallback})`
                : binding;
            lines.push(`      ${JSON.stringify(entry.name)}: ${value},`);
        }
        lines.push('    },');
    }
    lines.push('  });', '  return instance.exports;', '}');
    if (exportNames.length) lines.push('', `// Exported functions: ${exportNames.join(', ')}`);
    return lines.join('\n');
}

function analyze(root, mode) {
    const strings = new Map(), exportNames = [];
    const fnBodies = [], globalBodies = [], testBodies = [], benchBodies = [];
    const addBody = (bucket, node) => node && bucket.push(node);

    for (const item of root.namedChildren) {
        switch (item.type) {
            case 'fn_decl':
                addBody(fnBodies, childOfType(item, 'block'));
                break;
            case 'export_decl': {
                const fn = childOfType(item, 'fn_decl');
                exportNames.push(childOfType(fn, 'identifier').text);
                addBody(fnBodies, childOfType(fn, 'block'));
                break;
            }
            case 'global_decl':
                addBody(globalBodies, item.namedChildren.at(-1));
                break;
            case 'test_decl':
                if (mode === 'test') addBody(testBodies, childOfType(item, 'block'));
                break;
            case 'bench_decl':
                if (mode !== 'bench') break;
                const named = childOfType(item, 'setup_decl')?.namedChildren ?? [];
                benchBodies.push(...named.slice(0, -1));
                addBody(benchBodies, childOfType(named.at(-1), 'block'));
                break;
        }
    }

    for (const node of [...fnBodies, ...globalBodies, ...testBodies, ...benchBodies]) walk(node, child => {
        const value = stringLiteralValue(child);
        if (value !== null && !strings.has(value)) strings.set(value, strings.size);
    });
    return { strings: [...strings.keys()], exportNames };
}

function groupImportsByModule({ importFns, importVals }, profile = null) {
    const groups = new Map();
    const groupFor = (module) => {
        if (!groups.has(module)) groups.set(module, {
            module,
            entries: [],
            autoResolve: module.startsWith('node:'),
            ref: `__host_module_${groups.size}`,
        });
        return groups.get(module);
    };

    for (const entry of importFns) groupFor(entry.module).entries.push({ ...entry, kind: 'function' });
    for (const entry of importVals) groupFor(entry.module).entries.push({ ...entry, kind: 'value' });
    if (profile === 'ticks') groupFor('__utu_profile').entries.push({ name: 'tick', kind: 'function', exceptionFallback: null });
    return [...groups.values()];
}

function toBase64(bytes) {
    let bin = '';
    for (const byte of bytes) bin += String.fromCharCode(byte);
    return btoa(bin);
}
